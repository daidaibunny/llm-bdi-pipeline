def test_build_evaluation_result_dataset_separates_translation_and_execution_checks(
	tmp_path: Path,
) -> None:
	fixture = _write_fixture(tmp_path)

	result = build_evaluation_result_dataset(**fixture)

	assert result["translation"] == {
		"json_valid_count": 1,
		"dfa_equivalent_count": 1,
		"total": 1,
	}
	assert result["execution"]["success_count"] == 1
	assert result["execution"]["gold_dfa_accept_count"] == 1
	assert result["execution"]["prediction_dfa_accept_count"] == 1
	assert result["domains"][0]["candidate_count"] == 3
	assert result["domains"][0]["evidence_candidate_count"] == 1
	assert result["domains"][0]["schema_candidate_count"] == 2
	assert result["domains"][0]["selected_branch_count"] == 2
	assert result["profiles"][0]["median_action_count"] == 1
	assert result["conformance"]["semantic_success_count"] == 1
	assert result["conformance"]["zero_action_success_count"] == 1
	assert "provenance" not in result
	assert "run_id" not in json.dumps(result)
	assert "sha256" not in json.dumps(result)


def test_render_result_macros_uses_machine_counts(tmp_path: Path) -> None:
	result = {
		"benchmark": {"domain_count": 16, "problem_count": 1228},
		"translation": {
			"total": 475,
			"json_valid_count": 475,
			"dfa_equivalent_count": 475,
		},
		"execution": {
			"success_count": 1228,
			"jason_success_count": 1228,
			"val_success_count": 1228,
			"gold_dfa_accept_count": 1228,
			"prediction_dfa_accept_count": 1228,
			"median_action_count": 2,
			"median_runtime_seconds": 5.4,
		},
		"atomic": {
			"candidate_count": 100,
			"selected_branch_count": 80,
			"library_size_kib": 2.0,
		},
		"conformance": {
			"semantic_case_count": 14,
			"semantic_success_count": 14,
			"zero_action_case_count": 2,
			"zero_action_success_count": 2,
		},
	}

	macros = render_result_macros(result)
	result["domains"] = []
	result["profiles"] = []
	write_result_files(result, output_dir=tmp_path)

	assert DEFAULT_OUTPUT_DIR == PROJECT_ROOT / "artifacts/evaluation_tables"
	assert macros.startswith(
		"% Auto-generated by scripts/generate_evaluation_tables.py.\n"
	)
	assert "\\newcommand{\\TEGProblemCount}{1,228}" in macros
	assert "\\newcommand{\\TranslationEquivalentCount}{475}" in macros
	assert "\\newcommand{\\TEGMedianRuntimeSeconds}{5.4}" in macros
	assert "\\newcommand{\\AtomicRemovedBranchCount}{20}" in macros
	assert "\\newcommand{\\AtomicReductionPercent}{20}" in macros
	assert "\\newcommand{\\AtomicLibrarySizeKiB}{2}" in macros
	assert "\\newcommand{\\ConformanceSemanticSuccessCount}{14}" in macros
	assert "\\newcommand{\\ConformanceZeroActionSuccessCount}{2}" in macros
	assert (tmp_path / "evaluation_results.json").is_file()
	assert not (tmp_path / "paper_results.json").exists()


def test_render_domain_table_uses_readable_single_column_rows() -> None:
	result = {
		"domains": [
			{
				"domain": "toy",
				"train_count": 1,
				"test_count": 2,
				"evidence_candidate_count": 3,
				"schema_candidate_count": 4,
				"selected_branch_count": 5,
				"library_size_kib": 1.0,
				"execution_total": 2,
				"jason_success_count": 2,
				"val_success_count": 2,
				"gold_dfa_accept_count": 2,
				"prediction_dfa_accept_count": 2,
				"median_runtime_seconds": 1.5,
			},
		],
	}

	table = render_domain_table(result)

	assert "\\begin{table}[htbp]" in table
	assert "\\tiny" not in table
	assert "\\scriptsize" not in table
	assert "\\small" in table
	assert table.index("\\caption{") > table.index("\\end{tabular}")
	assert "\\begin{tabular}{lrrrrr}" in table
	assert "@{\\hspace" not in table
	assert "fixed seed-0 atomic cores" in table
	assert "hash-locked" not in table
	assert "Tr/Te" in table
	assert "E/S" in table
	assert "E2E" in table
	assert "toy & 1/2 & 3/4 & 5 & 2/2 & 1.5" in table
	assert "Jason, neutral-goal VAL, and both DFA checks" in table


def test_render_profile_table_compacts_redundant_success_oracles() -> None:
	result = {
		"benchmark": {"problem_count": 4},
		"translation": {
			"total": 2,
			"dfa_equivalent_count": 2,
		},
		"execution": {
			"success_count": 4,
			"median_action_count": 2,
			"median_runtime_seconds": 1.5,
		},
		"profiles": [
			{
				"profile": "ordered_two_milestone",
				"query_count": 4,
				"success_count": 4,
				"translation_count": 2,
				"dfa_equivalent_count": 2,
				"controller_compiled_count": 4,
				"jason_success_count": 4,
				"val_success_count": 4,
				"gold_dfa_accept_count": 4,
				"prediction_dfa_accept_count": 4,
				"median_action_count": 2,
				"median_runtime_seconds": 1.5,
			},
		],
	}

	table = render_profile_table(result)

	assert "\\begin{table}[htbp]" in table
	assert "\\tiny" not in table
	assert "\\scriptsize" not in table
	assert "\\small" in table
	assert "\\end{table}" in table
	assert table.index("\\caption{") > table.index("\\end{tabular}")
	assert "DFA equiv." in table
	assert "E2E valid" in table
	assert "Med. act." in table
	assert "Med. s" in table
	assert "Controller" not in table
	assert "Pred. DFA" not in table
	assert "Ordered-2 & 2/2 & 4/4 & 2 & 1.5" in table
	assert "All & 2/2 & 4/4 & 2 & 1.5" in table
	assert "pooled across all bound queries" in table


