#!/usr/bin/env python3
"""Freeze the registered paired compiler ablation result and LaTeX tables."""

from __future__ import annotations

import argparse
import json
from pathlib import Path
import sys
from typing import Any
from typing import Mapping

PROJECT_ROOT = Path(__file__).resolve().parents[1]
DEFAULT_OUTPUT_JSON = (
	PROJECT_ROOT / "paper_artifacts/gp2pl_evaluation/v1/paired_ablation_results.json"
)
DEFAULT_LATEX_OUTPUT_DIR = PROJECT_ROOT / "latex_code/aamas_method_paper/sections"
if str(PROJECT_ROOT) not in sys.path:
	sys.path.insert(0, str(PROJECT_ROOT))

from scripts.generate_aaai_comparison_tables import (  # noqa: E402
	build_paired_ablation_dataset,
)
from scripts.generate_aaai_comparison_tables import render_atomic_table  # noqa: E402
from scripts.generate_aaai_comparison_tables import (  # noqa: E402
	render_comparison_macros,
)
from scripts.generate_aaai_comparison_tables import render_temporal_table  # noqa: E402


def write_paired_ablation_files(
	result: Mapping[str, Any],
	*,
	output_json: str | Path,
	latex_output_dir: str | Path,
	update_manifest: bool,
) -> None:
	"""Write the portable paired result, generated tables, macros, and manifest."""

	json_path = Path(output_json).expanduser().resolve()
	latex_root = Path(latex_output_dir).expanduser().resolve()
	json_path.parent.mkdir(parents=True, exist_ok=True)
	latex_root.mkdir(parents=True, exist_ok=True)
	_write_json(json_path, result)
	generated_notice = "% Auto-generated by scripts/generate_aaai_comparison_tables.py."
	frozen_notice = "% Auto-generated by scripts/freeze_paired_ablation_results.py."
	(latex_root / "result_atomic_comparison_table.tex").write_text(
		render_atomic_table(result).replace(generated_notice, frozen_notice, 1),
		encoding="utf-8",
	)
	(latex_root / "result_temporal_comparison_table.tex").write_text(
		render_temporal_table(result).replace(generated_notice, frozen_notice, 1),
		encoding="utf-8",
	)
	(latex_root / "result_comparison_macros.tex").write_text(
		render_comparison_macros(result).replace(generated_notice, frozen_notice, 1),
		encoding="utf-8",
	)
	if update_manifest:
		_update_release_manifest(json_path.parent, result=result)


def _update_release_manifest(
	release_root: Path,
	*,
	result: Mapping[str, Any],
) -> None:
	manifest_path = release_root / "manifest.json"
	manifest = _read_json(manifest_path)
	manifest["paired_ablation_atomic_record_count"] = sum(
		int(row.get("test_count") or 0) for row in result["atomic"]
	)
	manifest["paired_ablation_temporal_record_count"] = sum(
		int(row.get("test_count") or 0) for row in result["temporal"]
	)
	manifest["files"] = [
		str(path.relative_to(release_root))
		for path in sorted(release_root.rglob("*"))
		if path.is_file() and path != manifest_path
	]
	_write_json(manifest_path, manifest)


def _read_json(path: Path) -> dict[str, Any]:
	payload = json.loads(path.read_text(encoding="utf-8"))
	if not isinstance(payload, dict):
		raise ValueError(f"JSON file is not an object: {path}")
	return payload


def _write_json(path: Path, payload: Mapping[str, Any]) -> None:
	path.write_text(
		json.dumps(payload, indent=2, sort_keys=True) + "\n",
		encoding="utf-8",
	)


def _parse_args() -> argparse.Namespace:
	parser = argparse.ArgumentParser(description=__doc__)
	parser.add_argument("--paired-results", type=Path, required=True)
	parser.add_argument("--challenge-summary", type=Path, required=True)
	parser.add_argument("--output-json", type=Path, default=DEFAULT_OUTPUT_JSON)
	parser.add_argument(
		"--latex-output-dir",
		type=Path,
		default=DEFAULT_LATEX_OUTPUT_DIR,
	)
	parser.add_argument("--no-update-manifest", action="store_true")
	return parser.parse_args()


def main() -> int:
	args = _parse_args()
	result = build_paired_ablation_dataset(
		paired_results_file=args.paired_results,
		challenge_summary_file=args.challenge_summary,
	)
	write_paired_ablation_files(
		result,
		output_json=args.output_json,
		latex_output_dir=args.latex_output_dir,
		update_manifest=not args.no_update_manifest,
	)
	print(
		f"frozen paired ablation atomic={len(result['atomic'])} "
		f"temporal={len(result['temporal'])} output={args.output_json}",
	)
	return 0


if __name__ == "__main__":
	raise SystemExit(main())
